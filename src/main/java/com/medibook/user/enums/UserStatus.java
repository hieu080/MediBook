package com.medibook.user.enums;

/**
 * UserStatus là các trạng thái của người dùng trong hệ thống, bao gồm:
 *
 * <ul>
 *     <li>ACTIVE: Người dùng đang hoạt động</li>
 *     <li>INACTIVE: Người dùng không hoạt động</li>
 *     <li>LOCKED: Người dùng bị khóa</li>
 * </ul>
 *
 * @author hieu080
 * @version 1.0
 * @since 2026-05-28
 */
public enum UserStatus {
    ACTIVE,
    INACTIVE,
    LOCKED,
}
